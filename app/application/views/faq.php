                    <p><strong><a id="q7"></a>1. How long do we need to run or walk for?</strong></p>
                    <p>It's up to you and the team captain. The only rule is that at least one team member is on the track at all times. Over the course of a Relay, an average team member might spend 2-3 hours on the track in total &ndash; most teams use a rota system to ensure that nobody is walking for too long a period. </p>
