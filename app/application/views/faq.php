                    <h3 style="background:#6a4d9b ">Cancer Survivors</h3>
                    <p><strong><a id="q14"></a>1. What is Cancer? </strong></p>
                    <p>Cancer is a disease caused by uncontrolled division of abnormal cells. Normally, cells divide to produce more cells only when the body needs them. If cells divide when new ones are not needed, they form a mass of excess tissue called a tumor. Tumors can be benign or malignant ( i.e. cancerous.)</p>
                    <p>The cells in a malignant tumor can invade and damage nearby tissues and organs. Cancer cells can also break away from malignant tumor and travel through the blood stream or lymphatic system to form new tumors in other parts of the body. The spreading of these cancerous cells is called metastasis.</p>
                    <p><strong><a id="q4"></a>1. Who is a cancer survivor? </strong></p>
                    <p>At Relay For Life, anyone who has received a cancer diagnosis is considered to be a cancer survivor. </p>                    <p><strong><a id="q5"></a>2. Who is a caregiver? </strong></p>
                    <p>A caregiver can be anyone that has supported you as a survivor, from taking you to the hospital or simply being there to listen. Caregivers do not need to register but if you provide the Relay committee with their T-shirt size we will provide them with a free T-shirt that they can wear on the day</p>
                    <p><strong><a id="q6"></a>3. What am I expected to do as a cancer survivor? </strong></p>
                    <p>Cancer survivors are the guests of honour at Relay For Life. You will meet other cancer Survivors and walk the first lap to officially open the event. There will be a special refreshments area at the event for you to relax and meet people and enjoy activities/entertainment. Attend the opening ceremony or stay for the whole Relay, it's up to you. </p>
